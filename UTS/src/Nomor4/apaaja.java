package Nomor4;

public class apaaja {
    public double hasil;
    public void kelilskuy(int a){
        hasil=(float) Math.PI*(2*a);
        System.out.println("Keliling dari lingkaran = "+ hasil);
    }
    public void luas(int a){
        hasil=(float) Math.PI*(Math.pow(a,2));
        System.out.println("Luas dari ligkaran = "+ hasil);
    }
}
